class BaseItem:
    def __init__(self, name, aliases):
        self.name = name
        self.aliases = aliases

class BaseManager:
    def __init__(self):
        self.items = {}
    
    def get_standard_name(self, alias):
        """별칭으로 표준 이름을 찾습니다."""
        for item in self.items.values():
            if alias in item.aliases:
                return item.name
        return None
    
    def get_item(self, name):
        """이름으로 아이템 객체를 반환합니다."""
        return self.items.get(name)
    
    def get_all_aliases(self, name):
        """아이템의 모든 별칭을 반환합니다."""
        item = self.items.get(name)
        return item.aliases if item else None
    
    def add_alias(self, name, new_alias):
        """새로운 별칭을 추가합니다."""
        item = self.items.get(name)
        if item:
            item.aliases.append(new_alias)
            return True
        return False

